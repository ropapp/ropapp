    private void SeleccionadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionadorActionPerformed
        JFileChooser selecFoto = (JFileChooser)evt.getSource();
        String command = evt.getActionCommand();
        if(command.equals(JFileChooser.APPROVE_SELECTION)){
            File archivoSeleccionado = selecFoto.getSelectedFile();
            Image imagen = new ImageIcon(dirreccion.getText()).getImage();
            Nuevo.dirreccion.setText(archivoSeleccionado.getAbsolutePath());
            Nuevo.foto.setImagen(imagen);
            dispose();
        }else if (command.equals(JFileChooser.CANCEL_SELECTION)){
            Nuevo obj=new Nuevo();
            obj.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_SeleccionadorActionPerformed

